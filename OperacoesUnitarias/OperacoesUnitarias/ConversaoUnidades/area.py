## Sistema métrico
def cm2_to_m2(x):
    return x * 1e-4


def m2_to_cm2(x):
    return x * 1e4


## Sistema inglês
def in2_to_ft2(x):
    return x / 144


def ft2_to_in2(x):
    return x * 144


